    private void btnAddBook1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddBook1ActionPerformed
        // TODO add your handling code here:
        Faker f = new Faker();
        txtAuthorName.setText(f.book().author());
        txtBookName.setText(f.book().title());
        txtIsbn.setText(f.code().isbn13());
        txtNumerOfPages.setText(Integer.toString(f.number().numberBetween(0, 1000)));
        txtPublicationYear.setText(Integer.toString(f.number().numberBetween(1900, 2021)));
        txtPublisher.setText(f.book().publisher());
        int rand = f.number().numberBetween(1, 4);
            jTextArea1.setText("");
        for (int i = 1; i <= rand; i++) {
            jTextArea1.append(f.code().ean13() + "\n");
        }
        rand = f.number().numberBetween(0, cmbSubject.getItemCount());
        cmbSubject.setSelectedIndex(rand);
        rand = f.number().numberBetween(0, cmbRack.getItemCount());
        cmbRack.setSelectedIndex(rand);
    }//GEN-LAST:event_btnAddBook1ActionPerformed

