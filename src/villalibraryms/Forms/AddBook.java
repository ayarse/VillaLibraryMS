    public String[] getTextLines() {
        int numLines = jTextArea1.getLineCount();
        String[] lines = new String[numLines];
        for (int i = 0; i < numLines; i++) {
            try {
                int start = jTextArea1.getLineStartOffset(i);
                int end = jTextArea1.getLineEndOffset(i); // treats last line specially
                if (i == numLines - 1) { // Last line
                    end++; // Allow for missing terminator
                }
                lines[i] = jTextArea1.getText(start, end - start - 1);
            } catch (BadLocationException e) { // should not happen
                throw new IllegalStateException("Could not read line " + i, e);
            }
        }
        return lines;
    }

